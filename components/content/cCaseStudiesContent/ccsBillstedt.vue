  Mit 70.234 Einwohner\*innen ist Billstedt nach Rahlstedt der Stadtteil mit der zweithöchsten Bevölkerungszahl in Hamburg. Auf eine Gesundheitseinrichtung kommen hier 474 Patient\*innen. Damit liegt Billstedt nach den von uns aufgestellten Grenzwerten in der Kategorie der niedrigen Versorgung. Mit 2 Krankenhäusern vor Ort und 716 Patient\*innen pro Fachpraxis ist die Versorgung jedoch auf den ersten Blick um einiges besser zu bewerten als in einigen Stadtteilen der gleichen Kategorie. Doch die Zahlen sind im Vergleich zu Eimsbüttel oder Blankenese ernüchternd: Auf eine der 47 Praxen für Allgemeinmedizin kommen 1755 Patient\*innen — in Blankenese sind es 609.  Eine der 10 Apotheken versorgt im Schnitt 7.023 Kund\*innen — in Eimsbüttel sind es hingegen 4.754.  Zudem verfügte Billstedt 2017 trotz seiner Größe über keine Physiotherapiepraxis.

